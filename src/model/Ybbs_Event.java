package model;

public class Ybbs_Event {
	
	private int evNumber;
	private String evSubject;
	private String evComment;
	private String evDate;
	private int evVisited;
	private String userId;
	public int getEvNumber() {
		return evNumber;
	}
	public void setEvNumber(int evNumber) {
		this.evNumber = evNumber;
	}
	public String getEvSubject() {
		return evSubject;
	}
	public void setEvSubject(String evSubject) {
		this.evSubject = evSubject;
	}
	public String getEvComment() {
		return evComment;
	}
	public void setEvComment(String evComment) {
		this.evComment = evComment;
	}
	public String getEvDate() {
		return evDate;
	}
	public void setEvDate(String evDate) {
		this.evDate = evDate;
	}
	public int getEvVisited() {
		return evVisited;
	}
	public void setEvVisited(int evVisited) {
		this.evVisited = evVisited;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}

	
}
